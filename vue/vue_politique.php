        Cookies
    </h2>
    <p>
        Notre site utilise des cookies pour améliorer votre expérience utilisateur et pour recueillir des informations sur votre utilisation de notre site web. Les cookies sont de petits fichiers texte qui sont stockés sur votre ordinateur ou votre appareil mobile lorsque vous visitez notre site. Nous utilisons des cookies de session pour vous identifier lorsque vous vous connectez à votre compte client, ainsi que des cookies persistants pour enregistrer vos préférences et pour suivre votre comportement de navigation sur notre site. Vous pouvez désactiver les cookies dans les paramètres de votre navigateur, mais cela peut affecter votre expérience utilisateur sur notre site.
