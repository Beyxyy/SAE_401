        Sécurité des informations personnelles
    </h2>
    <p>
        Nous prenons des mesures raisonnables pour protéger les informations personnelles que nous recueillons contre la perte, le vol, l'accès non autorisé, la divulgation, la modification ou la destruction. Nous utilisons des protocoles de sécurité standard pour protéger les informations de paiement, telles que le chiffrement SSL.
    </p>
    <h2>
        Formulaire de contact
    </h2>
    <p>
